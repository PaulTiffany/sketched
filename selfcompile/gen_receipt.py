def main() -> int:
    res = subprocess.run(["lake", "env", "lean", "ForcingAnalysis.lean"],
                         cwd=str(FA), capture_output=True, text=True)
    out = res.stdout + "\n" + res.stderr
    axioms = {m.group(1): [a.strip() for a in m.group(2).split(",") if a.strip()]
              for m in re.finditer(
                  r"'ForcingAnalysis\.([\w.]+)' depends on axioms: \[([^\]]*)\]", out)}

    for match in re.finditer(
            r"'ForcingAnalysis[.]([A-Za-z0-9_.]+)' does not depend on any axioms", out):
        axioms.setdefault(match.group(1), [])
    sorry_used = "declaration uses 'sorry'" in out
    theorems = [{"name": name, "file": file, "statement": stmt,
                 "axioms": axioms[name], "sorry": False, "status": "P"}
                for name, (file, stmt) in STMT.items() if name in axioms]

    receipt = {"schema_version": 2,
               "generated_from": "lake env lean ForcingAnalysis.lean (leanprover/lean4:v4.31.0)",
               "verified": len(theorems), "theorems": theorems}
    OUT.write_text(json.dumps(receipt, indent=2), encoding="utf-8")
    print(f"receipt -> {OUT}  ({len(theorems)} verified theorems)")
    for t in theorems:
        print(f"  {t['name']}: axioms {t['axioms']} sorry={t['sorry']}")
    if sorry_used:
        print("SORRY detected in Lean build output")
        return 1
    if not theorems:
        print("NO axiom lines parsed -- build output:\n", out[-800:])
    return 0 if theorems else 1


if __name__ == "__main__":
    sys.exit(main())
